/// Look up the embedded stub file content for a virtual path (e.g.
/// `"stubs/standard/standard_0.php"`). Returns `None` if the path isn't part
/// of the embedded set.
pub(crate) fn stub_content_for_path(path: &str) -> Option<&'static str> {
    STUB_FILES
        .iter()
        .find_map(|&(p, c)| if p == path { Some(c) } else { None })
}

/// Look up the stub virtual path that defines a built-in PHP function.
/// Lookup is case-insensitive (PHP function names are case-insensitive).
/// Returns `None` if the function isn't a known built-in.
pub(crate) fn stub_path_for_function(name: &str) -> Option<&'static str> {
    let lower = name.to_ascii_lowercase();
    STUB_FN_INDEX
        .binary_search_by(|(k, _)| (*k).cmp(lower.as_str()))
        .ok()
        .map(|i| STUB_FN_INDEX[i].1)
}

/// Look up the stub virtual path that defines a built-in PHP class / interface
/// / trait / enum. Lookup is case-insensitive. Strips a single leading
/// backslash if present. Returns `None` if not a known built-in type.
///
/// Public so the [`StubClassResolver`] in this crate (used by
/// `AnalysisSession` to make `find_class_like` aware of PHP built-ins)
/// can reach it, and so LSP consumers building their own chained
/// resolvers can compose it explicitly.
pub fn stub_path_for_class(fqcn: &str) -> Option<&'static str> {
    let trimmed = fqcn.strip_prefix('\\').unwrap_or(fqcn);
    let lower = trimmed.to_ascii_lowercase();
    STUB_CLASS_INDEX
        .binary_search_by(|(k, _)| (*k).cmp(lower.as_str()))
        .ok()
        .map(|i| STUB_CLASS_INDEX[i].1)
}

/// Look up the stub virtual path that defines a built-in PHP constant.
/// PHP constants are case-sensitive, so this lookup is exact.
pub(crate) fn stub_path_for_constant(name: &str) -> Option<&'static str> {
    let trimmed = name.strip_prefix('\\').unwrap_or(name);
    STUB_CONST_INDEX
        .binary_search_by(|(k, _)| (*k).cmp(trimmed))
        .ok()
        .map(|i| STUB_CONST_INDEX[i].1)
}

/// Scan PHP `source` for identifiers that match known built-in functions /
/// classes / constants, and return the deduplicated list of stub virtual
/// paths needed to cover them.
///
/// This is the core of the lazy-stub auto-discovery used by
/// [`crate::AnalysisSession::ensure_stubs_for_source`]. Uses the PHP lexer
/// to safely extract identifier tokens, avoiding manual byte-level scanning.
/// False positives (e.g., `imagecreate` appearing inside a string literal)
/// cost only one extra stub ingest — cheap and idempotent.
pub(crate) fn collect_referenced_builtin_paths(source: &str) -> Vec<&'static str> {
    use php_lexer::lex_all;

    let mut tokens: HashSet<&str> = HashSet::new();
    let (lexed, _errors) = lex_all(source);

    let mut i = 0;
    while i < lexed.len() {
        let token = &lexed[i];
        if token.kind == TokenKind::Identifier {
            let start = token.span.start as usize;
            let end = token.span.end as usize;
            if let Some(mut text) = source.get(start..end) {
                // Handle namespaced identifiers: Foo\Bar\Baz
                let mut j = i + 1;
                while j + 1 < lexed.len()
                    && lexed[j].kind == TokenKind::Backslash
                    && lexed[j + 1].kind == TokenKind::Identifier
                {
                    j += 2;
                    if let Some(part) = source.get(start..(lexed[j - 1].span.end as usize)) {
                        text = part;
                    }
                }
                tokens.insert(text);
                i = j;
            } else {
                i += 1;
            }
        } else {
            i += 1;
        }
    }

    let mut paths: HashSet<&'static str> = HashSet::new();
    for token in tokens {
        if let Some(p) = stub_path_for_function(token) {
            paths.insert(p);
        }
        if let Some(p) = stub_path_for_class(token) {
            paths.insert(p);
        }
        if let Some(p) = stub_path_for_constant(token) {
            paths.insert(p);
        }
    }
    paths.into_iter().collect()
}

/// Walk the parsed AST to find identifiers that match known built-in functions /
/// classes / constants, and return the deduplicated list of stub virtual paths.
///
/// Unlike [`collect_referenced_builtin_paths`], this walks the real AST instead
/// of doing a byte-level heuristic scan. This eliminates false positives from
/// function names appearing inside string literals or comments. Used by
/// [`crate::AnalysisSession::ensure_stubs_for_ast`] for single-file analysis
/// where the AST is already available.
pub(crate) fn collect_referenced_builtin_paths_from_ast(
    program: &php_ast::ast::Program<'_, '_>,
) -> Vec<&'static str> {
    let mut visitor = BuiltinRefVisitor {
        paths: HashSet::new(),
    };
    let _ = walk_program(&mut visitor, program);
    visitor.paths.into_iter().collect()
}

/// Visitor for extracting function/class/constant references from the AST.
struct BuiltinRefVisitor {
    paths: HashSet<&'static str>,
}

impl<'arena, 'src> Visitor<'arena, 'src> for BuiltinRefVisitor {
    fn visit_expr(&mut self, expr: &php_ast::ast::Expr<'arena, 'src>) -> ControlFlow<()> {
        match &expr.kind {
            ExprKind::FunctionCall(call) => {
                if let ExprKind::Identifier(name) = &call.name.kind {
                    if let Some(p) = stub_path_for_function(name.as_str()) {
                        self.paths.insert(p);
                    }
                }
            }
            ExprKind::New(new_expr) => {
                if let ExprKind::Identifier(name) = &new_expr.class.kind {
                    if let Some(p) = stub_path_for_class(name.as_str()) {
                        self.paths.insert(p);
                    }
                }
            }
            ExprKind::StaticMethodCall(call) => {
                if let ExprKind::Identifier(name) = &call.class.kind {
                    if let Some(p) = stub_path_for_class(name.as_str()) {
                        self.paths.insert(p);
                    }
                }
            }
            ExprKind::ClassConstAccess(access) => {
                if let ExprKind::Identifier(name) = &access.class.kind {
                    if let Some(p) = stub_path_for_class(name.as_str()) {
                        self.paths.insert(p);
                    }
                }
            }
            ExprKind::Identifier(name) => {
                if let Some(p) = stub_path_for_constant(name.as_str()) {
                    self.paths.insert(p);
                }
            }
            _ => {}
        }
        walk_expr(self, expr)
    }
}

