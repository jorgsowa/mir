/// Default-version entry point retained for callers (CLI, benches, tests) that
/// don't track a target PHP version. Equivalent to
/// [`load_stubs_for_version`] with `PhpVersion::LATEST`.
#[allow(dead_code)]
pub(crate) fn load_stubs(db: &mut MirDb) {
    load_stubs_for_version(db, PhpVersion::LATEST);
}

/// Load stubs filtered for `php_version`. Symbols whose `@since` post-dates
/// the target, or whose `@removed` is at or before the target, are skipped —
/// so multiple declarations of the same name (e.g. `each` on PHP 7 vs.
/// PHP 8) gated by `@since`/`@removed` collapse to the one matching variant.
#[allow(dead_code)]
pub(crate) fn load_stubs_for_version(db: &mut MirDb, _php_version: PhpVersion) {
    // Register each stub file's text as a salsa `SourceFile` input.
    // `find_class_like` / `find_function` resolve built-in PHP symbols by
    // routing through the `StubClassResolver` we install below.
    for (filename, content) in STUB_FILES {
        let arc_path: Arc<str> = Arc::from(*filename);
        let arc_content: Arc<str> = Arc::from(*content);
        db.upsert_source_file(arc_path, arc_content);
    }
    let resolver: Arc<dyn crate::ClassResolver> = Arc::new(crate::StubClassResolver);
    db.set_resolver(Some(resolver));
}

pub(crate) fn builtin_stub_slices_for_version(php_version: PhpVersion) -> Vec<StubSlice> {
    STUB_FILES
        .par_iter()
        .map(|(filename, content)| stub_slice_from_source(filename, content, Some(php_version)))
        .collect()
}

pub(crate) fn user_stub_slices(files: &[PathBuf], dirs: &[PathBuf]) -> Vec<StubSlice> {
    let mut all_paths: Vec<PathBuf> = files.to_vec();
    for dir in dirs {
        collect_stub_dir_paths(dir, &mut all_paths);
    }

    all_paths
        .par_iter()
        .filter_map(|path| parse_stub_file_slice(path))
        .collect()
}

pub(crate) fn stub_slice_from_source(
    filename: &str,
    content: &str,
    php_version: Option<PhpVersion>,
) -> StubSlice {
    let arena = crate::arena::create_parse_arena(content.len());
    let result = php_rs_parser::parse(&arena, content);
    let file: Arc<str> = Arc::from(filename);
    let collector =
        crate::collector::DefinitionCollector::new_for_slice(file, content, &result.source_map);
    let collector = match php_version {
        Some(version) => collector.with_php_version(version),
        None => collector,
    };
    let (mut slice, _) = collector.collect_slice(&result.program);
    mir_codebase::storage::deduplicate_params_in_slice(&mut slice);
    slice
}

fn parse_stub_file_slice(path: &Path) -> Option<StubSlice> {
    let content = match std::fs::read_to_string(path) {
        Ok(c) => c,
        Err(e) => {
            eprintln!("mir: cannot read stub file {}: {}", path.display(), e);
            return None;
        }
    };
    Some(stub_slice_from_source(
        path.to_string_lossy().as_ref(),
        &content,
        None,
    ))
}

pub(crate) fn collect_stub_dir_paths(dir: &Path, paths: &mut Vec<PathBuf>) {
    let entries = match std::fs::read_dir(dir) {
        Ok(e) => e,
        Err(e) => {
            eprintln!("mir: cannot read stub directory {}: {}", dir.display(), e);
            return;
        }
    };
    let mut dir_entries: Vec<PathBuf> = entries.filter_map(|e| e.ok().map(|e| e.path())).collect();
    dir_entries.sort_unstable();
    for path in dir_entries {
        if path.is_dir() {
            collect_stub_dir_paths(&path, paths);
        } else if path.extension().is_some_and(|e| e == "php") {
            paths.push(path);
        }
    }
}

/// Parse user-provided stub files and directories into `codebase`.
///
/// Called after built-in stubs are loaded so user definitions can override or
/// supplement built-ins (e.g. framework-specific classes, IDE helpers).
#[allow(dead_code)]
pub(crate) fn load_user_stubs(_db: &mut MirDb, files: &[PathBuf], dirs: &[PathBuf]) {
    // No-op: user stub registration now happens through shared_db::ingest_user_stubs
    // which registers SourceFile inputs directly for the pull-based salsa path.
    let _ = user_stub_slices(files, dirs);
}

